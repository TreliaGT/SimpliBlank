// Function to replace <i data-icon="iconName"></i> with the corresponding SVG
function loadIcons() {
    document.querySelectorAll('i[data-icon]').forEach(iTag => {
        const iconName = iTag.getAttribute('data-icon');
        const svgContent = icons[iconName];
        if (!svgContent) return;

        // Create the new <svg> element
        const svg = document.createElementNS("http://www.w3.org/2000/svg", "svg");
        svg.setAttribute("width", "24");
        svg.setAttribute("height", "24");
        svg.setAttribute("viewBox", "0 0 24 24");
        svg.setAttribute("fill", "none");
        svg.setAttribute("stroke", "currentColor");
        svg.setAttribute("stroke-width", "2");
        svg.setAttribute("stroke-linecap", "round");
        svg.setAttribute("stroke-linejoin", "round");
        svg.innerHTML = svgContent;

        // Replace <i> with <svg>
        iTag.replaceWith(svg);
    });
}


// Run the function to load icons when the page loads
window.onload = loadIcons;